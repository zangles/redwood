    public function endPeriod()
    {
        $currentPeriod = Period::getCurrentPeriod()[0];
        $currentPeriod = Period::find($currentPeriod->id);
        $currentPeriod->end_date = Carbon::now();
        $currentPeriod->save();

        $newPeriod = new Period();
        $newPeriod->start_date = Carbon::now();
        $newPeriod->save();

        return redirect()->route('period.index');
    }
